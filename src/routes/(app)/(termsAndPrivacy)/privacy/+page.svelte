<h3>How Our System Works</h3>
<p>
  Poketab Messenger is designed with security in mind. All your chats, whether
  with one person or a group, are end-to-end encrypted. This means your
  conversations are protected from the moment they leave your device until they
  arrive at the other end.
</p>
<p>Here's a quick rundown of how it works:</p>
<ul>
  <li>
    Our system creates a unique cryptographic key pair for you: a public key and
    a private key.
  </li>
  <li>
    The public key is shared with others so they can send you messages. The
    private key stays with you and is used to read the messages you receive.
  </li>
  <li>
    When you send a message, a new symmetric key (a special kind of key) is
    generated for that specific message.
  </li>
  <li>
    The message is encrypted with this symmetric key, and then the symmetric key
    itself is encrypted using each recipient's public key.
  </li>
  <li>
    Recipients use their private keys to decrypt the symmetric key, which then
    allows them to decrypt and read the message.
  </li>
</ul>
<p>
  This process ensures that each message is individually protected. Even the
  files you share are encrypted before they're temporarily stored on our
  servers. Once everyone has received the file, we delete it from our servers.
</p>

